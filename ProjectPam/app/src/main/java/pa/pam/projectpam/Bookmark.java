package pa.pam.projectpam;

public class Bookmark {
    public String nama;
    public String deskripsi;
    public String porsi;
    public String tanggal;
    public int gambar;

    public Bookmark(String nama, String deskripsi, String porsi, String tanggal, int gambar){
        this.nama = nama;
        this.deskripsi = deskripsi;
        this.porsi = porsi;
        this.tanggal = tanggal;
        this.gambar = gambar;
    }
}